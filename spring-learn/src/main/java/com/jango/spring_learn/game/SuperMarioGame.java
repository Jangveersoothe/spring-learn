package com.jango.spring_learn.game;

public class SuperMarioGame implements GamingConsole {
	public void up() {
		System.out.println("Go up");
	}

	public void down() {
		System.out.println("Go Down");
	}

	public void left() {
		System.out.println("Break");
	}

	public void right() {
		System.out.println("Shoot Bullet");
	}

}
